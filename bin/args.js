const yargs = require('yargs');
const validatePresentationMode = require('./validate/presentation-mode');

const validate = async parser => {
  const { argv } = parser;
  const { src, theme, port, title } = argv;

  return await validatePresentationMode(src, theme, port, title);
};

const args = () =>
  yargs
    .usage(`Usage: spectacle -s <file>`)

    // MDX File
    .option('src', {
      alias: 's',
      describe: 'Path to a file from which a presentation will be generated.',
      default: 'slides.mdx',
      type: 'string'
    })
    .option('theme', {
      alias: 't',
      describe: 'Path to a JS file that contains theme overrides.',
      type: 'string'
    })
    .option('title', {
      alias: 'l',
      describe: 'Title for the HTML file generated by the Spectacle CLI.',
      type: 'string'
    })
    .option('port', {
      alias: 'p',
      describe: 'Port for running the Spectacle development server.',
      type: 'number'
    })

    // Logistical
    .help()
    .alias('help', 'h')
    .version()
    .alias('version', 'v')
    .strict();

module.exports = {
  parse: () => validate(args())
};
